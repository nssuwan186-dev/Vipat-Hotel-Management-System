const thaiMonthMap: { [key: string]: number } = {
    'ม.ค.': 0, 'ก.พ.': 1, 'มี.ค.': 2, 'เม.ย.': 3, 'พ.ค.': 4, 'มิ.ย.': 5,
    'ก.ค.': 6, 'ส.ค.': 7, 'ก.ย.': 8, 'ต.ค.': 9, 'พ.ย.': 10, 'ธ.ค.': 11
};

const parseThaiDate = (dateStr: string): Date | null => {
    if (!dateStr || dateStr.trim() === '-' || dateStr.trim() === '') return null;
    const parts = dateStr.trim().replace(/\s+/g, ' ').split(' ');
    if (parts.length < 3) return null;
    const day = parseInt(parts[0], 10);
    const monthStr = parts[1];
    const month = thaiMonthMap[monthStr];
    let year = parseInt(parts[2], 10);

    if (year < 100) {
        year += 2500; // Convert BE YY to BE YYYY
    }
    year -= 543; // Convert BE to CE

    if (isNaN(day) || month === undefined || isNaN(year)) {
        console.warn(`Invalid date format, could not parse: "${dateStr}"`);
        return null;
    }
    return new Date(year, month, day);
};

const getRoomDetails = (roomNumber: string): { type: RoomType, price: number } => {
    const firstChar = roomNumber.charAt(0).toUpperCase();
    if (firstChar === 'A') return { type: 'Deluxe', price: 600 };
    if (firstChar === 'B') return { type: 'Standard Twin', price: 500 };
    if (firstChar === 'N') return { type: 'Suite', price: 800 };
    return { type: 'Standard', price: 500 };
};

